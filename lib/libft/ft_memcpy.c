#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char		*d;
	const unsigned char	*s;

	d = dest;
	s = src;
	if (!d && !s)
	{
		return (NULL);
	}
	while (n--)
	{
		*d++ = *s++;
	}
	return (dest);
}
